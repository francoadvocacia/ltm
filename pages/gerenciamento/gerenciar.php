	$(document).ready(function() {
		$('#usuarios').DataTable(
			{
				initComplete: function () {
					this.api().columns().every( function () {
						var column = this;
						var select = $('<select><option value="">Selecione...</option></select>')
							.appendTo( $(column.footer()).empty() )
							.on( 'change', function () {
								var val = $.fn.dataTable.util.escapeRegex(
									$(this).val()
								);

								column
									.search( val ? '^'+val+'$' : '', true, false )
									.draw();
							} );

						column.data().unique().sort().each( function ( d, j ) {
							if(column.search() === '^'+d+'$'){
								select.append( '<option value="'+d+'" selected="selected">'+d+'</option>' )
							} else {
								select.append( '<option value="'+d+'">'+d+'</option>' )
							}
						} );
					} );
				},
				pagingType: "simple_numbers",
				lengthMenu: [ [5, 10, 25, 50, -1], [5, 10, 25, 50, "All"] ],
				stateSave: true,
				scrollY: "450px",
				scrollX: false,
				scrollCollapse: true,
				language: {	
					"sEmptyTable": "Nenhum registro encontrado",
					"sInfo": "Mostrando de _START_ até _END_ de _TOTAL_ registros",
					"sInfoEmpty": "Mostrando 0 até 0 de 0 registros",
					"sInfoFiltered": "(Filtrados de _MAX_ registros)",
					"sInfoPostFix": "",
					"sInfoThousands": ".",
					"sLengthMenu": "Mostrar _MENU_ resultados por página",
					"sLoadingRecords": "Carregando...",
					"sProcessing": "Processando...",
					"sZeroRecords": "Nenhum registro encontrado",
					"sSearch": "Pesquisar",
					"oPaginate": {
						"sNext": "Próximo",
						"sPrevious": "Anterior",
						"sFirst": "Primeiro",
						"sLast": "Último"
					},
					"oAria": {
						"sSortAscending": ": Ordenar colunas de forma ascendente",
						"sSortDescending": ": Ordenar colunas de forma descendente"
					}
				}
			}
		);

		var table = $('#franco').DataTable();
		$('#franco tbody').on('dblclick', 'tr', function () {
			var data = table.row( this ).data();
			if (data[5] == ""){
				$obs = 'Nenhuma observação';
			}
			else {
				$obs = data[5];
			}
			alert('Cliente: '+ data[0] +
				'\nHorário marcado: ' + data[1] +
				'\nAgente Comercial: ' + data[2] +
				'\nCliente compareceu?: ' + data[3] + 
				'\nFechou contrato?: ' + data[4] +
				'\nObservações: ' + $obs );
		} );

		var table = $('#franco').DataTable();
		$('a.toggle-vis').on( 'click', function (e) {
			e.preventDefault();

			// Get the column API object
			var column = table.column( $(this).attr('data-column') );

			// Toggle the visibility
			column.visible( ! column.visible() );
		} );
	});
</script>
